def replace_timeline_line(html: str, new_timeline: str) -> str:
    """Timeline is a single line in all day*.html files."""
    lines = html.split("\n")
    for i, line in enumerate(lines):
        if '<div class="timeline">' in line:
            indent = line[: len(line) - len(line.lstrip())]
            lines[i] = indent + new_timeline.strip()
            return "\n".join(lines)
    raise ValueError("timeline line not found")


